void UART_DMA_Write(UART_HandleTypeDef *huart, uint8_t *data, uint16_t len) {
    if (huart == NULL || huart->Instance == NULL || data == NULL || len == 0) return;

    /* Max transfer size is 256 due to static buffers buf1/buf3 */
    uint16_t copy_len = (len > 256) ? 256 : len;

    /* Avoid busy-waiting that locks the RTOS */
    if (huart->gState != HAL_UART_STATE_READY) {
        return; /* Drop data instead of starving the system */
    }

    static uint8_t buf1[256];
    static uint8_t buf3[256];
    uint8_t *dest_buf = (huart->Instance == USART1) ? buf1 : buf3;

    /* Start new DMA transfer */
    memcpy(dest_buf, data, copy_len);
    HAL_UART_Transmit_DMA(huart, dest_buf, copy_len);
}

void UART_Printf_DMA(UART_HandleTypeDef *huart, const char *fmt, ...) {
    if (huart == NULL || huart->Instance == NULL) return;

    static char temp_buf[256];
    va_list args;
    va_start(args, fmt);
    int len = vsnprintf(temp_buf, 256, fmt, args);
    va_end(args);

    if (len > 0) {
        UART_DMA_Write(huart, (uint8_t *)temp_buf, (uint16_t)len);
    }
}
